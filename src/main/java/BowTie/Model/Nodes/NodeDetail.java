package BowTie.Model.Nodes;

/**
 * Enum for NodeDetail
 *
 * This enum is used to represent the different details that can be displayed for a node.
 * The details are the name, description, and score of the node.
 *
 * Name: The name of the node
 * Description: The description of the node - what it represents and what it does
 * Score: The score of the node based off of the type of node
 *
 */
public enum NodeDetail {
     NAME,  DESCRIPTION, SCORE
}
